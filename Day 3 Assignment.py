# You are given an integer array arr[]. The task is to find the sum of it.

# Examples:

# Input: arr[] = [1, 2, 3, 4]
# Output: 10
# Explanation: 1 + 2 + 3 + 4 = 10.
# Input: arr[] = [1, 3, 3]
# Output: 7
# Explanation: 1 + 3 + 3 = 7.

def sum_arr(arr):
    return sum(arr)
print(sum_arr([1,2,3,4,5]))