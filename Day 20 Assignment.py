# Lower case to upper case
# Difficulty: BasicAccuracy: 74.85%Submissions: 36K+Points: 1
# Given a string str containing only lowercase letters, generate a string with the same letters, but in uppercase.

# Example 1:

# Input:
# str = "geeks"
# Output: GEEKS
# Example 2:

# Input:
# str = "geeksforgeeks"
# Output: GEEKSFORGEEKS

def toUpper(s):
    return s.upper()
print(toUpper("geeks"))